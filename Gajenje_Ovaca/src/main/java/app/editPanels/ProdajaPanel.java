    private Vector vector2From(Ovca o){
        Vector v = new Vector(); 
        v.add(o.getNadimak());
        v.add(o.getOznaka());
        v.add(o.getPol());
        v.add(o.getStarost());
        if (o.getProdaja()==null){
            v.add(null);   
            v.add(null); 
            v.add(null); 
            v.add(null); 
            v.add(null); 
            v.add(o.getAktuelno());
        } else {
            v.add(o.getProdaja().getTezina());   
            v.add(o.getProdaja().getCenaKg()); 
            v.add(o.getProdaja().getCenaGrla()); 
            v.add(o.getProdaja().getKupac()); 
            v.add(o.getProdaja().getNapomena()); 
            v.add(o.getAktuelno());
        }
        v.add(o.getId());
        return v;
    }
    
    
